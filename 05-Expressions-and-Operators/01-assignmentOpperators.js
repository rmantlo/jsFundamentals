//assignment operator
let r = 5;

//additions assignment
r += 1; //r=r+1

//subtraction
r-= 1; // r=r-1

//multiplication
r *= 1; // r=r*1

//division

r/= 1; // r=r/1

//remainder (or modulus) assignment
r %= 1; // r= r % 1, ex: r=10, r%=4, remainer = 2 (for goes in twice, 2 left over)

//exponential assignment
r **= 1; // r = r ** 1 (r^1)